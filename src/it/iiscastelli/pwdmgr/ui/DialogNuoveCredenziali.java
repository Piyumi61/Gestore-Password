    private void btnAvantiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAvantiActionPerformed
        boolean campiValorizzati = controllaCampi();
        if(!campiValorizzati) {
            GestoreMessaggi.mostraErrore(this, "Devi compilare tutti i campi");
            return;
        }
        credenziali = new Credenziali(sitoApp, usernameEmail);
        DialogNuovaPassword dialog = new DialogNuovaPassword(padre, true);
        dialog.setCredenziali(credenziali);
        dispose();
        dialog.setVisible(true);
    }//GEN-LAST:event_btnAvantiActionPerformed

