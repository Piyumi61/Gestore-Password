    public void setCredenziali(Credenziali credenziali) {
        this.credenziali = credenziali;
        txtSitoApp.setText(credenziali.getSitoApp());
        txtUsernameEmail.setText(credenziali.getUsernameEmail());
    }
    
    private boolean controllaCampi() {
        sitoApp = txtSitoApp.getText();
        usernameEmail = txtUsernameEmail.getText();
        return !sitoApp.isBlank() && !usernameEmail.isBlank();
    }

